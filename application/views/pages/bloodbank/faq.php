		<h2>Why donate blood?</h2>
		<p>There is tremendous demand for blood in hospitals.
		 Many patients die because they are not able to cope with the loss of blood.</p>
		<p>Blood you donate is used to:</p>
		<ul>
			<li>Replace blood lost during injury as in accidents.</li>
			<li>Replace blood loss during major surgeries.</li>
			<li>Help patients with blood disorders like haemophilia, Von Willebrandâ€™s disease survive.</li>
			<li>Help burns patients receive plasma, that may be critical for their survival.</li>
			<li>Raise haemoglobin levels ( through transfusions) in patients with chronic ailments like kidney diseases, cancer and anaemia.</li>
		</ul>
		<p>Blood donation hardly makes a difference in terms of health to the donor, but it can help save the life of a patient.</p>
    
    
		<h2>Who can donate blood?</h2>
		<p>Some basic health conditions have to be met by donors:</p>
		<p>A donor should :</p>
		<ul>
			<li>Be above 18 years and below 60 years of age.</li>
			<li>Have a haemoglobin count that is not less than 12.5 g/dl.</li>
			<li>Weigh not less than 45 kgs.</li>
			<li>Have normal body temperature at the time of donation.</li>
			<li>Have normal BP at the time of donation.</li>
			<li>Should be free of any disease at the time of donation .</li>
		</ul>
		<h2>Who should not donate blood?</h2>
		<p>The following categories of people should avoid giving blood:</p>
		<ul>
			<li>Pregnant or lactating women, or those who have recently had an abortion.</li>
			<li>Persons who are on steroids, hormonal supplements or certain specified medication.</li>
			<li>Persons with multiple sexual partners or those who are addicted to drugs.</li>
			<li>Persons who have had an attack of infection like jaundice, rubella, typhoid or malaria.</li>
			<li>Persons who have undergone surgery in the previous six months.</li>
			<li>Persons who have consumed alcohol in the 24 hours prior to donation.</li>
			<li>Women should avoid donation during their menstruating period.</li>
			<li>Those who have undergone various vaccinations should avoid donation for the corresponding period specified below:
				<table class="table-2 table table-striped table-bordered">
				<tr><th>Type of Vaccine</th><th>The period in which donation should be avoided</th></tr>
				<tr><td>Hepatitis B</td><td>6 months</td></tr>
				<tr><td>Live vaccines </td><td>2 weeks</td></tr>
				<tr><td>Killed vaccines</td><td>48 Hours</td></tr>
				<tr><td>Rabies</td><td>1 year</td></tr>
				</table>
			</li>
			<li>Persons with any systemic disease like heart disease, kidney disease, liver problems, blood disorders or asthma should NOT donate blood.</li>
			<li>Persons suffering from infections transmitted through transfusions like HIV, Hepatitis, Syphilis etc should Not donate blood. </li>
		</ul>
    	
    	<h2>How much blood is taken at the time of donation? How soon does the body make good this loss?</h2>
		<p>Only 350 ml of blood is taken at the time of donation. An average person has 5-6 litres of blood in the body.In terms of volume the loss is corrected in 24-48 hours by the body. The red cell count is corrected in about 56 days.</p>
		
		<h2>How long does the process of donation take?</h2>
		<p>The actual bleeding time is about 5-6 minutes. There will be a medical check up before this and you will be advised some rest ( for 5-10 minutes) and given some refreshment after donation. The whole process takes about 30 minutes.</p>
		
		<h2>How often can one donate blood?</h2>
		<p>The minimum time advised between two donations is 3 months. This gap helps blood regain the normal haemoglobin count.</p>

		<h2>Are there any hazards in blood donation? What are the precautions that need to be taken?</h2>
		<p>Your health will not suffer because of the blood you have donated. In fact, the bone marrow is stimulated to produce new cells.However if conditions are not hygienic, you may be exposed to infection.
		<b>Precaution:</b> Be sure that disposable needles are used for bleeding.</p>

		<h2>What happens to the blood that is donated?</h2>
		<p>The blood is screened for the following diseases/infections:</p>
		<ul>
			<li>HIV</li>
			<li>Hepatitis B & C</li>
			<li>Syphilis</li>
			<li>Malaria</li>
		</ul>
		<p>The blood is grouped and stored either as whole blood or as components like Packed red blood cells, plasma or platelets. This is then sent on demand to hospitals.</p>
		
		<h2>How is Blood Grouped?</h2>
		<p>
		Blood is composed of cells suspended in a liquid.
		The liquid portion is the plasma, from which therapeutic fractions and derivatives are made.</p>
		<p>Suspended in the plasma are three types of cells:</p>
		<ul>
			<li>Red cells: These carry Oxygen</li>
			<li>White cells: These fight infection</li>
			<li>Platelets: These stop wounds bleeding</li>
		</ul>
		<p>The most common type of grouping is the ABO grouping. Red Blood Cells have a protein coat on their surface which distinguishes them. According to this blood is divided into four groups:</p>
		<p>A (A protein is present), B (B protein is present),AB (AB protein is present)and O ( No protein is present).</p>
